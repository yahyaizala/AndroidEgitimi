package tk.kydev;

public class Matematik {
    public  int topla(int x,int y)
    {
        return x+y;
    }
    public int cikar(int x,int y)
    {
        return x-y;
    }
    public int carp(int x,int y)
    {
        return x*y;
    }
    public double bol(double x,double y)
    {
        return x/y;
    }

}
